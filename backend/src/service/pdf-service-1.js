// 🔥 MAIN FUNCTION
export const generateAndUploadPdf = async (checkpointId, title, markdownContent) => {
  try {
    // 1. Markdown → HTML
    const htmlContent = marked(markdownContent);

    // 2. Wrap ke template
    const finalHtml = buildHtml(title, htmlContent);

    // 3. Generate PDF
    const file = { content: finalHtml };

    const pdfBuffer = await html_to_pdf.generatePdf(file, {
      format: "A4",
      margin: {
        top: "20mm",
        bottom: "20mm",
        left: "15mm",
        right: "15mm"
      }
    });

    // 4. Upload ke Supabase
    const fileName = `checkpoints/${checkpointId}.pdf`;

    const { error } = await supabaseAdmin.storage
      .from("materials")
      .upload(fileName, pdfBuffer, {
        contentType: "application/pdf",
        upsert: true
      });

    if (error) {
      throw new Error(`Upload error: ${error.message}`);
    }

    // 5. Ambil URL
    const { data } = supabaseAdmin.storage
      .from("materials")
      .getPublicUrl(fileName);

    return data.publicUrl;

  } catch (error) {
    console.error("[PDF ERROR]", error);
    throw error;
  }
};